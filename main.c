	// Set up graphics window
	hidePlayfieldAndSprites();
	setPlayerCursorVisible(0);
	loadColorTable(dialogColorTable);
	setBackgroundGradient(gradient);
	clearTextWindow();
	setTextWindowColorTheme(1);
